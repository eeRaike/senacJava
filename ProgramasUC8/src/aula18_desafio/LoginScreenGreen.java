    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        String username = txtUsername.getText();
        String password = new String(pswPassword.getPassword());
        UserRegister op = new UserRegister();
        
        if(op.loginValidation(username, password)){
            ProgramScreen program = new ProgramScreen();
            program.setVisible(true);
            this.dispose();
        }else{
            System.out.println("nuh uh");
            JOptionPane.showMessageDialog(null,"Login inv√°lido tente novamente");
            tryCounter++;
            System.out.println(tryCounter);
        }
        
        if(tryCounter >= 3){
        txtUsername.setEnabled(false);
        pswPassword.setEnabled(false);
        btnLogin.setEnabled(false);
        String msg = "Limite de tentativas excedido feche o programa";
        JOptionPane.showMessageDialog(rootPane, msg);
        }
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

